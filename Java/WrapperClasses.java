public class WrapperClasses {
    public static void main(String[] args) {
        // wrapper classes are used on PRIMITIVE data types so that they can be used as
        // REFERENCE data types - in other words - can use string methods

        // Primative     // Wrapper

        // boolean       // Boolean
        // char          // Character
        // int           // Integer
        // double        // Double

        Boolean a = true;
        Character b = 'Q';
        Integer c = 9;
        Double d = 3.14159;

        // These wrapper classes can still be treated like primitive types with the process of
        // "unboxing"
    }
}
