public class Variables {
    public static void main(String[] args) {

        int x; // declaration
        x = 123; // assignment

        int y = 456; // initialization

        System.out.println(x + y);
        System.out.println("x + y");
        System.out.println(x + "" + y);

        String name = "Quinten";

        System.out.println(name);
    }
}
